/** 상속은 어제 사용할까?
 *  코드의 재사용 관점에서 inheritance(상속) 와 composition 가 있다.
 *  inheritance(상속)
 *      일반적인(공통적인) 개념에서 구체적인 개념의 관계
 *      상위클래스와 하위클래스 개념
 *      클래스간의 겨합도가 높은 관계
 *      상위 클래스 수정이 하위 클래스에 미치는 영향이 큼
 *      계층 구조가 복잡하거나 계층의 길이가 너무 길면(hierarchy가 높으면) 좋지 않

 *  composition
 *      코드 재사용의 가장 일반적 방법
 *      클래스가 다른 클래스를 포함하는 관계 (변수로 선언)
 *      Library를 구현할 때 ArrayList를 사용
 *      상속하지 않음
 */
